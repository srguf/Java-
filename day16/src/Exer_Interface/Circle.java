package Exer_Interface;

public class Circle {
	private double redius;
	
	public Circle() {
		super();
	}
	public Circle(double redius) {
		super();
		this.redius = redius;
	}
	
	public double getRedius() {
		return redius;
	}
	public void setRedius(double redius) {
		this.redius = redius;
	}
}
