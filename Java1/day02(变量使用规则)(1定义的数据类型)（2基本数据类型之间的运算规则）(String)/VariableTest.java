/*
变量的使用
1.java定义变量的格式： 数据类型 变量名 = 变量值；

2.说明：
1变量必须先声明，后使用
2变量都定义在其定义域内，且仅在域内有效         (我们访问时使用变量名访问)
3同一作用域内，不可声明两个同名变量
*/

class VariableTest{
	public static void main(String[] args){
		int myAge = 12;
		System.out.println(myAge);
		
		int myNumber;
		myNumber = 1001;
		System.out.println(myNumber);
	}
}
















